<p>Les produits sont proposés à la vente jusqu'à épuisement du stock. En cas de commande d'un produit devenu indisponible, le client sera informé de cette indisponibilité, dans les meilleurs délais, par courrier électronique ou par courrier postal.</p>


<h4>Article 4 Prix des produits</h4>

<p>La rubrique " Catalogue " de notre site indique les prix en euros toutes taxes comprises, hors frais de port. Le montant de la TVA est précisé lors de la sélection d'un produit par le client et les frais de port apparaissent sur l'écran à la fin de la sélection des différents produits par le client.</p>

<p>La société . . . . . . se réserve le droit de modifier ses prix à tout moment mais les produits commandés sont facturés au prix en vigueur lors de l'enregistrement de la commande.</p>

<p>Les tarifs proposés comprennent les rabais et ristournes que l'entreprise . . . . . . . . . serait amenée à octroyer compte tenu de ses résultats ou de la prise en charge par l'acheteur de certaines prestations.</p>

<p>Aucun escompte ne sera accordé en cas de paiement anticipé.</p>


<h4>Article 5 Commande</h4>

<p>Le client valide sa commande lorsqu'il active le lien " Confirmez votre commande " en bas de la page " Récapitulatif de votre commande " après avoir accepté les présentes conditions de vente. Avant cette validation, il est systématiquement proposé au client de vérifier chacun des éléments de sa commande; il peut ainsi corriger ses erreurs éventuelles.</p>

<p>La société . . . . . . confirme la commande par courrier électronique; cette information reprend notamment tous les éléments de la commande et le droit de rétractation du client.</p>

<p>Les données enregistrées par la société . . . . . . constituent la preuve de la nature, du contenu et de la date de la commande. Celle-ci est archivée par la société . . . . . . dans les conditions et les délais légaux; le client peut accéder à cet archivage en contactant le service Relations Clients.</p>


<h4>Article 6 Modalités de paiement</h4>

<p>Le règlement des commandes s'effectue par chèque ou carte bancaire.</p>

<p>Lors de l'enregistrement de la commande, l'acheteur devra verser un acompte de 10 % du montant global de la facture, le solde devant être payé à réception des marchandises</p>


<h4>Article 7 Retard de paiement</h4>

<p>En cas de défaut de paiement total ou partiel des marchandises livrées au jour de la réception, l'acheteur doit verser à l'entreprise . . . . . . . . . . . une pénalité de retard égale à une fois et demi le taux de l'intérêt légal.</p>

<p>Le taux de l'intérêt légal retenu est celui en vigueur au jour de la livraison des marchandises.</p>

<p>Cette pénalité est calculée sur le montant hors taxes de la somme restant due, et court à compter de la date d'échéance du prix sans qu'aucune mise en demeure préalable ne soit nécessaire.</p>

<p>Conformément aux articles 441-6 c. com. et D. 441-5 c. com., tout retard de paiement entraine de plein droit, outre les pénalités de retard, une obligation pour le débiteur de payer une indemnité forfaitaire de 40€ pour frais de recouvrement.</p>

<p>Une indemnité complémentaire pourra être réclamée, sur justificatifs, lorsque les frais de recouvrement exposés sont supérieurs au montant de l'indemnité forfaitaire.</p>


<h4>Article 8 Livraison</h4>

<p>Tout produit est livré sans garantie quant aux délais, exception faite des livraisons aux particuliers. La date limite de livraison varie suivant leur adresse. Elle est fixée, pour une adresse en France métropolitaine, au jour du paiement + 8 jours et, pour les autres destinations, au jour du paiement + 1 mois.</p>



